// Automatically extract icons used in the codebase
// This script scans all Vue and TS files for icon usage and generates the offline bundle
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { glob } from 'glob';
import fluentIcons from '@iconify-json/fluent/icons.json' with { type: 'json' };
import fluentColorIcons from '@iconify-json/fluent-color/icons.json' with { type: 'json' };

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Scan source files for icon usage
function scanForIcons() {
    const srcDir = path.join(__dirname, '..', 'src');
    const files = glob.sync('**/*.{vue,ts,tsx,js,jsx}', { cwd: srcDir, absolute: true });

    const iconSet = new Set();

    // Regex patterns to match icon usage
    // Matches: icon="fluent:icon-name" or icon='fluent:icon-name' or icon="fluent-color:icon-name"
    const iconPattern = /icon=["']([^"']+)["']/g;

    files.forEach(file => {
        const content = fs.readFileSync(file, 'utf8');
        let match;

        while ((match = iconPattern.exec(content)) !== null) {
            const iconName = match[1];
            iconSet.add(iconName);
        }
    });

    return Array.from(iconSet).sort();
}

// Separate icons by prefix
function categorizeIcons(icons) {
    const fluent = [];
    const fluentColor = [];

    icons.forEach(icon => {
        if (icon.startsWith('fluent-color:')) {
            fluentColor.push(icon.replace('fluent-color:', ''));
        } else if (icon.startsWith('fluent:')) {
            fluent.push(icon.replace('fluent:', ''));
        } else {
            // Assume fluent if no prefix
            fluent.push(icon);
        }
    });

    return { fluent, fluentColor };
}

// Extract icon data
function extractIconData(iconNames, iconCollection) {
    const extracted = {
        prefix: iconCollection.prefix,
        icons: {},
        width: iconCollection.width || 24,
        height: iconCollection.height || 24
    };

    const missing = [];

    iconNames.forEach(iconName => {
        if (iconCollection.icons[iconName]) {
            extracted.icons[iconName] = iconCollection.icons[iconName];
        } else {
            missing.push(iconName);
        }
    });

    return { extracted, missing };
}

// Main execution
console.log('🔍 Scanning codebase for icon usage...');
const foundIcons = scanForIcons();
console.log(`✓ Found ${foundIcons.length} unique icons in use`);

const { fluent, fluentColor } = categorizeIcons(foundIcons);
console.log(`  - ${fluent.length} Fluent icons`);
console.log(`  - ${fluentColor.length} Fluent Color icons`);

// Extract Fluent icons
const { extracted: extractedFluent, missing: missingFluent } = extractIconData(fluent, fluentIcons);
console.log(`\n📦 Extracted ${Object.keys(extractedFluent.icons).length} Fluent icons`);
if (missingFluent.length > 0) {
    console.warn(`⚠️  Missing Fluent icons: ${missingFluent.join(', ')}`);
}

// Extract Fluent Color icons
const { extracted: extractedFluentColor, missing: missingFluentColor } = extractIconData(fluentColor, fluentColorIcons);
console.log(`📦 Extracted ${Object.keys(extractedFluentColor.icons).length} Fluent Color icons`);
if (missingFluentColor.length > 0) {
    console.warn(`⚠️  Missing Fluent Color icons: ${missingFluentColor.join(', ')}`);
}

// Generate TypeScript file
const outputPath = path.join(__dirname, '..', 'src', 'utils', 'iconify-offline.ts');
const tsContent = `// Offline icon configuration for Iconify
// Auto-generated by scripts/auto-extract-icons.js - do not edit manually
// Run: npm run extract-icons to regenerate
// 
// This file is automatically generated by scanning the codebase for icon usage.
// Icons found: ${foundIcons.length} (${fluent.length} Fluent, ${fluentColor.length} Fluent Color)

import { addCollection } from '@iconify/vue'

const fluentIcons = ${JSON.stringify(extractedFluent, null, 2)}

const fluentColorIcons = ${JSON.stringify(extractedFluentColor, null, 2)}

export function initializeOfflineIcons() {
  addCollection(fluentIcons)
  addCollection(fluentColorIcons)
}
`;

fs.writeFileSync(outputPath, tsContent, 'utf8');
console.log(`\n✅ Written to ${outputPath}`);
console.log(`\n💡 Icons are now bundled and ready for offline use!`);
